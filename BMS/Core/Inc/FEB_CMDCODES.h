#ifndef INC_FEB_CMDCODES_H_
#define INC_FEB_CMDCODES_H_
//**************** 16 bit CMD CODE NAMES *********************
//CONFIG           BIT:FEDCBA9876543210
#define WRCFGA		(0b0000000000000001)
#define WRCFGB		(0b0000000000100100)
#define RDCFGA		(0b0000000000000010)
#define RDCFGB		(0b0000000000100110)

//READ CELL V REG  BIT:FEDCBA9876543210
#define RDCVA 		(0b0000000000000100)
#define RDCVB 		(0b0000000000000110)
#define RDCVC 		(0b0000000000001000)
#define RDCVD 		(0b0000000000001010)
#define RDCVE 		(0b0000000000001001)
#define RDCVF 		(0b0000000000001011)
#define RDCVALL		(0b0000000000001100)

//READ AVG V REG   BIT:FEDCBA9876543210
#define RDACA		(0b0000000001000100)
#define RDACB		(0b0000000001000110)
#define RDACC		(0b0000000001001000)
#define RDACD		(0b0000000001001010)
#define RDACE		(0b0000000001001001)
#define RDACF		(0b0000000001001011)
#define RDACALL		(0b0000000001001100)

//READ S V REG     BIT:FEDCBA9876543210
#define RDSVA		(0b0000000000000011)
#define RDSVB		(0b0000000000000101)
#define RDSVC		(0b0000000000000111)
#define RDSVD		(0b0000000000001101)
#define RDSVE		(0b0000000000001110)
#define RDSVF		(0b0000000000001111)
#define RDSALL		(0b0000000000010000)
#define RDCSALL		(0b0000000000010001)
#define RDACSALL	(0b0000000001010001)

//READ FLTRD REG   BIT:FEDCBA9876543210
#define RDFCA		(0b0000000000010010)
#define RDFCB		(0b0000000000010011)
#define RDFCC		(0b0000000000010100)
#define RDFCD		(0b0000000000010101)
#define RDFCE		(0b0000000000010110)
#define RDFCF		(0b0000000000010111)
#define RDFCALL		(0b0000000000011000)

//READ AUX REG/STE BIT:FEDCBA9876543210
#define RDAUXA		(0b0000000000011001)
#define RDAUXB		(0b0000000000011010)
#define RDAUXC		(0b0000000000011011)
#define RDAUXD		(0b0000000000011111)
#define RDRAXA		(0b0000000000011100)
#define RDRAXB		(0b0000000000011101)
#define RDRAXC		(0b0000000000011110)
#define RDRAXD		(0b0000000000100101)
#define RDSTATA		(0b0000000000110000)
#define RDSTATB		(0b0000000000110001)
#define RDSTATC		(0b0000000000110010)
#define RDSTATC_ERR	(0b0000000001000000)
#define RDSTATD		(0b0000000000110011)
#define RDSTATE		(0b0000000000110100)
#define RDASALL		(0b0000000000110101)

//W|R PWM REG GRP  BIT:FEDCBA9876543210
#define WRPWMA		(0b0000000000100000)
#define RDPWMA		(0b0000000000100010)
#define WRPWMB		(0b0000000000100001)
#define RDPWMB		(0b0000000000100011)

//LPCM CMDS        BIT:FEDCBA9876543210
#define CMDIS		(0b0000000001000000)
#define CMED		(0b0000000001000001)
#define CMHB		(0b0000000001000011)
#define WRCMCFG		(0b0000000001011000)
#define RDCMCFG		(0b0000000001011001)
#define WRCMCELLT	(0b0000000001011010)
#define RDCMCELLT	(0b0000000001011011)
#define WRCMGPIOT	(0b0000000001011100)
#define RDCMGPIOT	(0b0000000001011101)
#define CLRCMFLAG	(0b0000000001011110)
#define RDCMFLAG	(0b0000000001011111)

//START/POLL ADC   BIT:FEDCBA9876543210
#define ADCV		(0b0000001001100000)
#define ADSV		(0b0000000101101000)
#define AD_RD		(0b0000000100000000)
#define AD_CONT		(0b0000000010000000)
#define AD_DCP		(0b0000000000010000)
#define AD_RSTF		(0b0000000000000100)
#define AD_OW1		(0b0000000000000010)
#define AD_OW0		(0b0000000000000001)
#define ADAX		(0b0000010000010000)
#define ADAX2		(0b0000010000000000)
#define ADAX_CH4	(0b0000000001000000)
#define ADAX_CH3	(0b0000000000001000)
#define ADAX_CH2	(0b0000000000000100)
#define ADAX_CH1	(0b0000000000000010)
#define ADAX_CH0	(0b0000000000000001)
#define ADAX_OW		(0b0000000100000000)
#define ADAX_PUP	(0b0000000010000000)

//CLEAR REG CMDS   BIT:FEDCBA9876543210
#define CLRCELL		(0b0000011100010001)
#define CLRFC		(0b0000011100010100)
#define CLRAUX		(0b0000011100010010)
#define CLRSPIN		(0b0000011100010110)
#define CLRFLAG		(0b0000011100010111)
#define CLOVUV		(0b0000011100010101)

//POLL ADC         BIT:FEDCBA9876543210
#define PLADC		(0b0000011100011000)
#define PLCADC		(0b0000011100011000)
#define PLSADC		(0b0000011100011001)
#define PLAUX		(0b0000011100011010)
#define PLAUX2		(0b0000011100011011)

//W|R COMM REG GRP BIT:FEDCBA9876543210
#define WRCOMM		(0b0000011100100001)
#define RDCOMM		(0b0000011100100010)
#define STCOMM		(0b0000011100100011)

//MISC             BIT:FEDCBA9876543210
#define MUTE		(0b0000000000101000)
#define UNMUTE		(0b0000000000101001)
#define RDSID		(0b0000000000101100)
#define RSTCC		(0b0000000000101110)
#define SNAP		(0b0000000000101101)
#define UNSNAP		(0b0000000000101111)
#define SRST		(0b0000000000100111)

//RETENTION REG    BIT:FEDCBA9876543210
#define ULRR		(0b0000000000111000)
#define WRRR		(0b0000000000111001)
#define RDRR		(0b0000000000111010)
#endif
